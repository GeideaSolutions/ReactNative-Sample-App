- (NSURL *)sourceURLForBridge:(RCTBridge *)bridge
{
#if DEBUG
  return [[RCTBundleURLProvider sharedSettings] jsBundleURLForBundleRoot:@"index"];
#else
  return [[NSBundle mainBundle] URLForResource:@"main" withExtension:@"jsbundle"];
#endif
}

- (void)applicationWillResignActive:(UIApplication *)application {
    UIView *securityScreen = [[UIView alloc] initWithFrame:self.window.bounds];
    securityScreen.tag = 1234; // Tag for identification
    securityScreen.backgroundColor = [UIColor blackColor];
    [self.window addSubview:securityScreen];
}

- (void)applicationDidBecomeActive:(UIApplication *)application {
    UIView *securityScreen = [self.window viewWithTag:1234];
    if (securityScreen) {
        [securityScreen removeFromSuperview];
    }
}

